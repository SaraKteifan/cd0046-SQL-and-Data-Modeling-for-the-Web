@app.route('/venues')
def venues():
  # TODO: replace with real venues data. (DONE xxx)
  #       num_upcoming_shows should be aggregated based on number of upcoming shows per venue.
  # data=[{
  #   "city": "San Francisco",
  #   "state": "CA",
  #   "venues": [{
  #     "id": 1,
  #     "name": "The Musical Hop",
  #     "num_upcoming_shows": 0,
  #   }, {
  #     "id": 3,
  #     "name": "Park Square Live Music & Coffee",
  #     "num_upcoming_shows": 1,
  #   }]
  # }, {
  #   "city": "New York",
  #   "state": "NY",
  #   "venues": [{
  #     "id": 2,
  #     "name": "The Dueling Pianos Bar",
  #     "num_upcoming_shows": 0,
  #   }]
  # }]

  subquery = db.session.query(
    Show.venue_id,
    func.count(Show.id).label('num_upcoming_shows')
  ).filter(
      Show.start_time > datetime.now()
  ).group_by(
      Show.venue_id
  ).subquery()

  query = db.session.query(
      Venue.city,
      Venue.state,
      func.json_agg(
          func.json_build_object(
              'id', Venue.id,
              'name', Venue.name,
              'num_upcoming_shows', func.coalesce(subquery.c.num_upcoming_shows, 0)
          )
      ).label('venues')
  ).outerjoin(
      subquery, Venue.id == subquery.c.venue_id
  ).group_by(
      Venue.city,
      Venue.state
  )

  data = query.all()

  return render_template('pages/venues.html', areas=data)

